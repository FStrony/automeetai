# We import the Streamlit module with the alias 'st'
# This way, we can use 'st' to access Streamlit's features
import streamlit as st

# We use the title() function from Streamlit to add a title to our web app
st.title('Hello World App')

# We use the write() function to display simple text on the page
# This function is very versatile and can also show data and display charts
st.write('Hello World!')
